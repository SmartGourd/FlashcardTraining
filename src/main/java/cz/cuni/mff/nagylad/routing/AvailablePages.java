package cz.cuni.mff.nagylad.routing;

/**
 * Enum representing the available pages in the application.
 * Used to navigate between different sections of the application.
 */
public enum AvailablePages {

    /** The main menu page of the application. */
    MAIN_PAGE,

    /** The page listing all available sets and to create new ones. */
    SETS_PAGE,

    /** The detail page for a specific set, allowing editing and launch of training. */
    SET_DETAIL_PAGE,

    /** The training page where users can practice term-definition pairs. */
    TRAINING_PAGE,

    /** The game page where users can view and upgrade their city using gems. */
    GAME_PAGE
}

